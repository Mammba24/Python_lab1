# Исходные данные
money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен

# Инициализация переменных
months = 0

# Расчет
while True:
    # Определяем текущий бюджет, складывая зарплату и оставшиеся сбережения
    budget = money_capital + salary

    # Проверяем, хватает ли бюджета на текущие расходы
    if budget >= spend:
        # Увеличиваем количество месяцев, которые можно прожить
        months += 1
        # Уменьшаем подушку безопасности на разницу между тратами и зарплатой
        money_capital -= (spend - salary)
    else:
        # Если бюджет не покрывает расходы, прекращаем цикл
        break

    # Увеличиваем расходы на 5% для следующего месяца
    spend *= (1 + increase)

print("Количество месяцев, которое можно протянуть без долгов:", months)

